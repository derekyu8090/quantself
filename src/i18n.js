export function useTranslation(lang) {
  const t = (path) => {
    const keys = path.split('.');
    let val = translations[lang];
    for (const k of keys) {
      if (val == null) return path;
      val = val[k];
    }
    return val ?? path;
  };
  return { t, lang };
}

export default translations;
